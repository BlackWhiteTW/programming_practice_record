#include <bits/stdc++.h>

using namespace std;

int main()
{
    int week;
    cin >> week;
    if (week == 1)
        cout << "Monday" << endl;
    else if (week == 2)
        cout << "Tuesday" << endl;
    else if (week == 3)
        cout << "Wednesday" << endl;
    else if (week == 4)
        cout << "Thursday" << endl;

    return 0;
}